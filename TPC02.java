public class TPC02 {
    public static void main(String[] args) {
        // 프로그래밍의 3대 요소: 변수, 자료형(데이터 타입), 할당 연산자
        int a, b, c;
        a = 1;
        b = 1;
        c = a + b;
        System.out.println(c);

        float f = 34.5f;
        System.out.println(f);

        char d = 'A';
        System.out.println(d);

        boolean bool = true;
        System.out.println(bool);
    }
}
